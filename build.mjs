/* ---------- платформа: цвет + тип ---------- */
const PCOLOR = { telegram: "#57a6e6", twinby: "#a98bff", whatsapp: "#4ad17a", instagram: "#e472b0", tinder: "#ff6b5e", unknown: "#9aa0a8" };
const DATING = new Set(["twinby", "tinder"]);

const esc = (s) => String(s).replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
const TAG_RE = /^\[(ПРАВО|ЛЕВО) (?:(\S+) )?(текст|медиа|голосовое)(?: \{Ответ на: "(.*?)"\})?\] ?([\s\S]*)$/;

/* ---------- парсинг сырого вывода ---------- */
function parse(raw) {
  const lines = raw.split("\n");
  const head = {};
  const get = (re) => (raw.match(re) || [])[1] || "";
  head.platform = get(/Тип платформы:\s*(.+)/);
  head.left = get(/Пользователь слева:\s*(.+)/);
  head.right = get(/Пользователь справа:\s*(.+)/);
  head.extra = get(/Дополнительно:\s*(.+)/);
  head.key = head.platform.trim().toLowerCase();

  const start = lines.findIndex((l) => l.trim() === "Разметка сообщений");
  const msgs = [];
  for (const line of lines.slice(start + 1)) {
    const m = line.match(TAG_RE);
    if (m) msgs.push({ side: m[1] === "ПРАВО" ? "r" : "l", time: m[2], type: m[3], reply: m[4] || "", text: m[5] || "" });
    else if (msgs.length) msgs[msgs.length - 1].text += "\n" + line;
  }
  for (const m of msgs) m.text = m.text.replace(/^\n+|\n+$/g, "");
  return { head, msgs };
}

/* ---------- раскраска сырого вывода ---------- */
function rawHtml(raw) {
  let section = "head";
  return raw.split("\n").map((line) => {
    if (line.trim() === "Разметка сообщений") { section = "msgs"; return "Разметка сообщений"; }
    if (section === "head") return line.trim() === "" ? "" : `<span class="h">${esc(line)}</span>`;
    const m = line.match(/^(\[(ПРАВО|ЛЕВО)[^\]]*\])([\s\S]*)$/);
    if (m) return `<span class="${m[2] === "ПРАВО" ? "r" : "l"}">${esc(m[1])}</span>${esc(m[3])}`;
    return esc(line);
  }).join("\n");
}

/* ---------- pretty-вид сообщения ---------- */
function msgHtml(m) {
  const reply = m.reply ? `<div class="reply"><b>Ответ на:</b> <q>${esc(m.reply)}</q></div>` : "";
  let payload;
  if (m.type === "голосовое") {
    const i = m.text.indexOf(" Расшифровка:");
    payload = i >= 0
      ? `<span class="mk">${esc(m.text.slice(0, i))}</span>${esc(m.text.slice(i))}`
      : `<span class="mk">${esc(m.text)}</span>`;
  } else if (m.type === "медиа") {
    payload = `<span class="mk">${esc(m.text)}</span>`;
  } else {
    payload = esc(m.text);
  }
  const ts = m.time ? `<span class="ts">${esc(m.time)}</span>` : "";
  return `<div class="msg ${m.side}">
              <div class="msg-head"><span class="tag ${m.side}">${m.side === "r" ? "ПРАВО" : "ЛЕВО"}</span>${ts}<span class="ty">${m.type}</span></div>
              ${reply}<p class="text">${payload}</p>
            </div>`;
}

